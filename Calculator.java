
public class Calculator {

    public Calculator(){}

    public double add(double x, double y)
    {
        double sum;

        sum = x + y;

        return sum;
    }

    public double subtract(double x, double y)
    {
        double difference;

        difference = x - y;

        return difference;
    }

    public double divide(double x, double y)
    {
        double quotient;

        quotient = x/y;

        return quotient;
    }

    public double multiply(double x, double y)
    {
        double product;

        product = x * y;

        return product;
    }
}
